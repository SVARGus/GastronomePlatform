namespace GastronomePlatform.Common.Application.Abstractions
{
    /// <summary>
    /// Генератор URL-friendly slug-идентификаторов из произвольного текста.
    /// Используется при создании сущностей с человекочитаемыми URL
    /// (например, <c>Dish.Slug</c>, <c>Category.Slug</c>).
    /// </summary>
    /// <remarks>
    /// Этап 2: реализация ASCII-only — кириллица транслитерируется в латиницу,
    /// остальные нелатинские символы заменяются на дефисы. Уникальность slug в БД
    /// разрешается на уровне Application-слоя через добавление суффикса <c>-N</c>.
    /// Альтернативные стратегии (UTF-8 кириллица, мультиязычные slug-и) — Этап 8+.
    /// </remarks>
    public interface ISlugGenerator
    {
        /// <summary>
        /// Преобразует исходный текст в URL-friendly slug.
        /// </summary>
        /// <param name="source">Исходный текст (например, <c>Name</c> блюда).</param>
        /// <returns>
        /// Slug в нижнем регистре, состоящий из латинских букв, цифр и дефисов.
        /// Возвращает пустую строку, если результат после транслита и нормализации пуст
        /// (например, исходный текст состоит только из эмодзи или неподдерживаемых символов).
        /// </returns>
        string Generate(string source);
    }
}
