namespace GastronomePlatform.Modules.Subscriptions.Domain.Enums
{
    /// <summary>
    /// Природа оффера (тарифного предложения) в каталоге. Самодокументирует
    /// строку <c>PlanPrice</c>: что это за предложение и по каким правилам
    /// с ним обращаться. Хранится как <c>int</c> в БД. Используется
    /// в <c>PlanPrice.Kind</c>.
    /// </summary>
    public enum OfferKind
    {
        /// <summary>
        /// Бесплатный пробный период. Списаний нет; по окончании — переход
        /// на оффер <c>RenewsAsPriceId</c> и немедленная попытка списания.
        /// </summary>
        Trial = 0,

        /// <summary>
        /// Вводный покупаемый оффер со скидкой и/или короткой длительностью.
        /// По окончании продлевается в базовый оффер через <c>RenewsAsPriceId</c>.
        /// </summary>
        Intro = 1,

        /// <summary>
        /// Базовый оффер без скидки. Целевой оффер для продления
        /// (<c>RenewsAsPriceId = null</c> — продлевается собой).
        /// </summary>
        Standard = 2,

        /// <summary>
        /// Удержательный оффер с большой скидкой и расширенными грантами.
        /// Непокупаемый напрямую (<c>IsPurchasable = false</c>);
        /// предлагается только в сценарии отмены, старт отложенный.
        /// </summary>
        Retention = 3,

        /// <summary>
        /// Системный удешевлённый оффер. Непокупаемый; переход на него
        /// только при провале рекуррентных списаний (dunning-лестница).
        /// </summary>
        DunningFallback = 4
    }
}
