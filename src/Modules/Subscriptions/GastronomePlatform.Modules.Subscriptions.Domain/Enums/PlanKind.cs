namespace GastronomePlatform.Modules.Subscriptions.Domain.Enums
{
    /// <summary>
    /// Род тарифного плана. Разграничивает базовую подписку (тарифный уровень)
    /// и докупаемые услуги «à la carte». Хранится как <c>int</c> в БД.
    /// Используется в <c>SubscriptionPlan.PlanKind</c>.
    /// </summary>
    /// <remarks>
    /// У пользователя может быть только одна активная <see cref="Base"/>-подписка
    /// (несёт роль, имеет покупочный роль-гейт), но сколько угодно активных
    /// <see cref="AddOn"/>-подписок (роль-агностичны, покупочного гейта не имеют).
    /// </remarks>
    public enum PlanKind
    {
        /// <summary>Базовый тарифный уровень. ≤1 активной на пользователя, несёт роль, имеет покупочный роль-гейт.</summary>
        Base = 0,

        /// <summary>Докупаемая услуга «à la carte». Живёт параллельно Base, роль не несёт, роль-гейта не имеет. Этап 4+.</summary>
        AddOn = 1
    }
}
