namespace GastronomePlatform.Modules.Subscriptions.Domain.Enums
{
    /// <summary>
    /// Услуги (доступы к функционалу) платформы, которые несут подписки и промоакции.
    /// Хранится как <c>int</c> в БД. Используется в <c>PlanGrant.Grant</c>
    /// и <c>PromotionGrant.Grant</c>. Значения начинаются с 1 (не битовая маска —
    /// одна строка <c>PlanGrant</c> = один грант, а не набор).
    /// </summary>
    /// <remarks>
    /// <para>
    /// Гранты бывают трёх видов: булевый (есть/нет), уровневый (ранг приоритета)
    /// и квотовый (несёт число через <c>PlanGrant.Quantity</c>).
    /// </para>
    /// <para>
    /// Реестр усвоения (какой грант требует какой роли в рантайме) —
    /// в <c>Authorization/FeatureGrantRoleRequirements</c>. По умолчанию грант
    /// агностичен (<c>null</c>); роль-привязанные гранты продвижения и рекламы
    /// требуют роли <c>Chef</c>.
    /// </para>
    /// <para>
    /// Гранты 5–8 (продвижение/реклама) на Этапе 3 инертны до потребителя:
    /// выдаются, но ни на что не влияют, пока Dishes не построит ранжирование
    /// и учёт расхода квоты (Этап 4+). Гранты 1–4 (потребительские) — рабочие сразу.
    /// </para>
    /// </remarks>
    public enum FeatureGrant
    {
        /// <summary>Доступ к блюдам с полными рецептами и пошаговыми инструкциями. Булевый, агностик.</summary>
        FullRecipes = 1,

        /// <summary>Калькулятор расчёта порций. Булевый, агностик.</summary>
        PortionCalculator = 2,

        /// <summary>Дополнительный список рецептов (сезонные). Булевый, агностик.</summary>
        SeasonalRecipes = 3,

        /// <summary>Особые категории рецептов. Булевый, агностик.</summary>
        SpecialCategories = 4,

        /// <summary>
        /// Базовое продвижение под блюдами (приоритет над теми, у кого продвижения нет).
        /// Уровневый (tier-1). Требует роли <c>Chef</c>. Этап 4+.
        /// </summary>
        PromotionBasic = 5,

        /// <summary>
        /// Доп. продвижение (приоритет над базовым). Уровневый (tier-2) + квотовый:
        /// число блюд через <c>PlanGrant.Quantity</c> (<c>null</c> = безлимит).
        /// Требует роли <c>Chef</c>. Этап 4+.
        /// </summary>
        PromotionAdvanced = 6,

        /// <summary>
        /// Реклама на дашбордах (ведёт на страницу повара со списком блюд).
        /// Уровневый (база). Требует роли <c>Chef</c>. Этап 4+.
        /// </summary>
        DashboardAds = 7,

        /// <summary>Расширенная реклама на дашбордах. Уровневый. Требует роли <c>Chef</c>. Этап 4+.</summary>
        DashboardAdsExtended = 8
    }
}
